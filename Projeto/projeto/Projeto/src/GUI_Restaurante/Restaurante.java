    private void selectExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_selectExcluirActionPerformed
        // TODO add your handling code here:
        String scpf = JOptionPane.showInputDialog (null, "Informe o CPF à ser deletado", "Excluir cliente",
    JOptionPane.QUESTION_MESSAGE);
        int cpf = Integer.parseInt(scpf);
        
        try{
            resta.remover(cpf);
        }
        catch (Exception e){
            JOptionPane.showMessageDialog (null, "Não foi possível remover", "ERRO",
    JOptionPane.WARNING_MESSAGE);
        }
        
    }//GEN-LAST:event_selectExcluirActionPerformed

    private void setPreparandoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_setPreparandoActionPerformed
        // TODO add your handling code here:
        String sid = setID.getText();
        int id      = Integer.parseInt(sid); 
        setID.setText("");
        try{
            boolean b = resta.AlterarStatus("Preparando", id);
            if (!b)
                JOptionPane.showMessageDialog (null, "Há um erro de comunicação com o servidor ou o ID não é existente", "ERRO",
    JOptionPane.WARNING_MESSAGE);
        }
        
        catch (Exception e){
            JOptionPane.showMessageDialog (null, "Falha de comunicação com o servidor", "ERRO",
    JOptionPane.WARNING_MESSAGE);
        }
        
    }//GEN-LAST:event_setPreparandoActionPerformed

