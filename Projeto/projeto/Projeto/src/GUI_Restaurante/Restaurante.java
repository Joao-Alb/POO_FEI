    String lista = "ID     CPF       Pedido       Status";
    String checklista = lista;
    public Restaurante() {
        initComponents();
        
        try {
            ResultSet rs = resta.Lista();
            while (rs.next()) {
                lista = lista + ("\n"+rs.getInt("ID") + "    " +rs.getString("CPF") + "     " + 
                        rs.getString("Pedido")+"        "+rs.getString("Status"));
                
            }
            if(lista.equals(checklista)){
                lista = "Não há nada na fila";
                jTextArea.setText(lista);
            }
            else{
                jTextArea.setText(lista);
            }
        } 
        
        catch( Exception e) {
            lista = "Erro na conexão com o Banco de Dados";
            jTextArea.setText(lista);
        } 
    }

